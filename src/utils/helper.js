export const convertCodeToString = (code) => {
  let string;
  switch (code) {
    case "district":
      string = "Malalayang";
      break;
    case "village":
      string = "Malalayang";
      break;
    case "regency":
      string = "Malalayang";
      break;
    default:
      break;
  }

  return string;
};
